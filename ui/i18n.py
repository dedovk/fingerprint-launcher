TRANSLATIONS = {"uk": UK, "en": EN, "ru": RU, "fr": FR, "es": ES}


def tr(lang: str, key: str) -> str:
    primary = TRANSLATIONS.get(lang, UK)
    if key in primary:
        return primary[key]
    extra = EXTRA_TRANSLATIONS.get(lang, EXTRA_TRANSLATIONS["uk"])
    return extra.get(key, EXTRA_TRANSLATIONS["uk"].get(key, UK.get(key, key)))


def action_labels(lang: str) -> dict[str, str]:
    return {
        definition.command_type: tr(lang, definition.label_key)
        for definition in ACTION_DEFINITIONS
    }


def localized_winbio_message(lang: str, hr: int) -> str:
    from core.winbio import hresult_message_key

    return tr(lang, hresult_message_key(hr))


def localized_finger_name(lang: str, sub_factor: int) -> str:
    from core.winbio import finger_name_key

    key, values = finger_name_key(sub_factor)
    return tr(lang, key).format(**values)
